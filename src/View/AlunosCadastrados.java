    public void mostrarLista() {
    
    DefaultTableModel modelo = (DefaultTableModel) jTableAlunos.getModel();
    modelo.setNumRows(0);
    
    jTableAlunos.getColumnModel().getColumn(0).setPreferredWidth(20);
    jTableAlunos.getColumnModel().getColumn(1).setPreferredWidth(20);
    jTableAlunos.getColumnModel().getColumn(2).setPreferredWidth(20);
    jTableAlunos.getColumnModel().getColumn(3).setPreferredWidth(20);
    jTableAlunos.getColumnModel().getColumn(4).setPreferredWidth(20);
    jTableAlunos.getColumnModel().getColumn(5).setPreferredWidth(20);
    jTableAlunos.getColumnModel().getColumn(6).setPreferredWidth(20);
    
        try {
            Connection conexao = new Conexao().getConnection();
            AlunosDAO alunosdao = new AlunosDAO(conexao);
            
            for(Alunos a: alunosdao.listar()){
                
                modelo.addRow(new Object[]{
                
                a.getNomeAluno(),
                a.getDataNascimentoAluno(),
                a.getCpfAluno(),
                a.getEmailAluno(),
                a.getTelefoneAluno(),
                a.getPlanoAluno(),
                a.getDataInicioAluno()
                
                });
                
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(AlunosCadastrados.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }
    
    

